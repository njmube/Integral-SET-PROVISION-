        private List<Object[]> executeHQLQuery(String hql) {
        try {
            Session session = HibernateUtil.getSessionFactory().openSession();
            session.beginTransaction();
            Query q = session.createQuery(hql);
            List resultList = q.list();
            session.getTransaction().commit();
            session.disconnect();
            return resultList;
        }catch (HibernateException he) {
            he.printStackTrace();
            List lista= null;
            return lista;
        }
    }
    
    private String guardarMarca(Cuentas obj) 
    {
        Session session = HibernateUtil.getSessionFactory().openSession();
        String IdMarca = null;
        try 
        {
            session.beginTransaction();
            IdMarca=(String) session.save(obj);
            session.save(obj);
            session.getTransaction().commit();
        } 
        catch (HibernateException he) 
        {
            he.printStackTrace();
            session.getTransaction().rollback();
            IdMarca= null;
        }   
        finally
        {
            session.close();
            return IdMarca;
        }
    }
    
    public boolean consultaMarca(String id_marca)
    {
        List <Object[]> resultList=executeHQLQuery("from Cuentas obj where obj.idCuentas='"+id_marca+"'");
        if(resultList.size()>0)
            return true;
        else
            return false;
    }
    
