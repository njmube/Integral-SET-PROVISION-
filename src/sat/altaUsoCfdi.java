    private List<Object[]> executeHQLQuery(String hql) {
        try {
            Session session = HibernateUtil.getSessionFactory().openSession();
            session.beginTransaction();
            Query q = session.createQuery(hql);
            List resultList = q.list();
            session.getTransaction().commit();
            session.disconnect();
            return resultList;
        }catch (HibernateException he) {
            he.printStackTrace();
            List lista= null;
            return lista;
        }
    }
    public boolean consultaCFDI(String id)
    {
        List <Object[]> resultList=executeHQLQuery("from UsoCfdi obj where obj.idUsoCfdi='"+id+"'");
        if(resultList.size()>0)
            return true;
        else
            return false;
    }
    
    private String guardarUso(UsoCfdi obj) 
    {
        Session session = HibernateUtil.getSessionFactory().openSession();
        String Id = null;
        try 
        {
            session.beginTransaction();
            Id=(String) session.save(obj);
            session.save(obj);
            session.getTransaction().commit();
        } 
        catch (HibernateException he) 
        {
            he.printStackTrace();
            session.getTransaction().rollback();
            Id = null;
        }   
        finally
        {
            session.close();
            return Id;
        }
    }
        
