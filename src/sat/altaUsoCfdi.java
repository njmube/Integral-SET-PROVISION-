    private void b_guardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_guardar1ActionPerformed
        h=new Herramientas(user, 0);
        h.session(sessionPrograma);               
        if(t_id.getText().compareTo("")!=0)
        {
            if(this.t_descripcion.getText().compareTo("")!=0)
            {
                if(consultaCFDI(t_id.getText())==false)
                {
                    UsoCfdi nuevoUso = new UsoCfdi();
                    nuevoUso.setIdUsoCfdi(t_id.getText());
                    nuevoUso.setDescripcion(t_descripcion.getText());
                    nuevoUso.setFisica(cb_fisica.getSelectedItem().toString());
                    nuevoUso.setMoral(cb_moral.getSelectedItem().toString());
                    String respuesta=guardarUso(nuevoUso);
                    if(respuesta==null)
                        JOptionPane.showMessageDialog(null, "Error al guardar los datos");
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Registro almacenado");
                        this.borra_cajas();
                        t_id.requestFocus();
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "¬°El ID ya existe!");
                    this.borra_cajas();
                    t_id.requestFocus();
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "¬°Debe introducir la descripci√≥n del uso CFDI");            
                t_descripcion.requestFocus();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "¬°Debe introducir el ID del Uso CFDI!");            
            t_id.requestFocus();
        }
    }//GEN-LAST:event_b_guardar1ActionPerformed

